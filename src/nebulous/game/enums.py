class SplitMultiplier(enum.Enum):
    (
        X8,
        X16,
        X32,
        X64,
    ) = range(4)

    @classmethod
    def from_net(cls, value: int) -> "SplitMultiplier":
        match value:
            case 0x08:
                return cls.X8
            case 0x10:
                return cls.X16
            case 0x20:
                return cls.X32
            case 0x40:
                return cls.X64
            case _:
                raise ValueError(f"Invalid SplitMultiplier value: {value}")


